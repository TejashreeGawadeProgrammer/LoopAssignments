package loopAssighnments;

public class PrimeDemo {
	public static void main(String args[])
	{
		int n=29;
		for(int i=2;i<=n;i++){    
		    if(n%i!=0){    
		     System.out.println(n+" is prime number");        
		     }
		    else {
		    	System.out.println("The Number is not prime number");
		    }
		   } 
		
	}
}
